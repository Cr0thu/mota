def _load_mota_first10():
    """讀取 mota_first10.json 並轉換為 _difference_data 期望的 7 元組。

    僅使用 MT1~MT10 共 10 樓,將 H5mota 的格子數字映射到 database 編號。
    其他資料 (maps/enemies/items/icons) 復用 _default_data 中的設定。
    """
    with open(_MOTA_FIRST10_JSON, encoding='utf-8') as f:
        raw = json.load(f)

    floor_ids = [f'MT{i}' for i in range(1, 11)]
    width = raw['floors'][floor_ids[0]]['width']
    height = raw['floors'][floor_ids[0]]['height']
    layer = len(floor_ids)

    # 轉換地圖數字編號
    map_3d = []
    for fid in floor_ids:
        src = raw['floors'][fid]['map']
        new_floor = [[_FIRST10_CELL_MAP.get(c, 0) for c in row] for row in src]
        map_3d.append(new_floor)

    # 標記玩家初始位置 (cell = 7)
    hero = raw['firstData']['hero']
    z0 = floor_ids.index(raw['firstData'].get('floorId', 'MT1'))
    y0, x0 = hero['loc']['y'], hero['loc']['x']
    map_3d[z0][y0][x0] = 7

    # 將 MT10 通往不存在的下一層的樓梯改為通關終點 (cell = 8)
    last_cf = raw['floors'][floor_ids[-1]].get('changeFloor', {})
    for key, info in last_cf.items():
        if info.get('floorId') == ':next':
            x_str, y_str = key.split(',')
            map_3d[layer - 1][int(y_str)][int(x_str)] = 8

    # 轉換 changeFloor: 找目標樓層中的反向樓梯位置
    change_floor = {}
    for idx, fid in enumerate(floor_ids):
        cf = raw['floors'][fid].get('changeFloor', {})
        for key, info in cf.items():
            x_str, y_str = key.split(',')
            cx, cy = int(x_str), int(y_str)
            tf = info.get('floorId')
            if tf == ':next':
                t_idx = idx + 1
            elif tf == ':before':
                t_idx = idx - 1
            elif isinstance(tf, str) and tf.startswith('MT'):
                try:
                    t_idx = int(tf[2:]) - 1
                except ValueError:
                    continue
            else:
                continue
            if t_idx < 0 or t_idx >= layer:
                continue
            cur = map_3d[idx][cy][cx]
            seek = 43 if cur == 42 else (42 if cur == 43 else None)
            target = None
            if seek is not None:
                for ty in range(height):
                    for tx in range(width):
                        if map_3d[t_idx][ty][tx] == seek:
                            target = (t_idx, ty, tx)
                            break
                    if target:
                        break
            if target is None:
                stair = info.get('stair')
                fallback = 42 if stair == 'upFloor' else 43
                for ty in range(height):
                    for tx in range(width):
                        if map_3d[t_idx][ty][tx] == fallback:
                            target = (t_idx, ty, tx)
                            break
                    if target:
                        break
            if target is not None:
                change_floor[(idx, cy, cx)] = target

    floors = {
        'layer': layer,
        'width': width,
        'height': height,
        'map': map_3d,
        'changeFloor': change_floor,
        'disable': {
            (3, 1, 6), (3, 1, 6, 1), (3, 1, 6, 2), (3, 1, 6, 3)
        },
        'afterEvent': {}
    }
    _INF = 999999999
    # 初始属性设置（原版10层魔塔）
    player = {
        'lv': 1,
        'maxhp': 999999,
        'hp': 800,
        'atk': 10,
        'def': 10,
        'mdef': 0,
        'money': 0,
        'exp': 0,
        'items': {'yellowKey': 0, 'blueKey': 0, 'redKey': 0}
    }
    # ------------------------------------------------------------------------
    #  解析 H5mota 動態事件
    #  劇情事件(events): 直接使用觸發後狀態，應用到 map_3d
    #  戰鬥後事件(afterBattle): 保留動態屬性，轉為 afterEvent
    # ------------------------------------------------------------------------
    # 建立 id -> number 反向查詢表，因為 setBlock 的 number 可能是 ID 字符串
    _h5_id_to_num = {}
    for k, v in raw.get('maps', {}).items():
        if v and isinstance(v, dict) and 'id' in v:
            _h5_id_to_num[v['id']] = int(k)

    def _apply_event_changes(event_data, z_idx):
        """遞迴提取 openDoor，直接應用到 map_3d（只保留門被打開後的狀態）"""
        if isinstance(event_data, dict):
            etype = event_data.get('type')
            if etype == 'openDoor':
                locs = event_data.get('loc', [])
                if isinstance(locs, (list, tuple)) and len(locs) == 2 and not isinstance(locs[0], (list, tuple)):
                    locs = [locs]
                for loc in locs:
                    if isinstance(loc, (list, tuple)) and len(loc) == 2:
                        ex, ey = loc
                        map_3d[z_idx][ey][ex] = 0  # 門打開後變為地板
            else:
                for v in event_data.values():
                    _apply_event_changes(v, z_idx)
        elif isinstance(event_data, list):
            for item in event_data:
                _apply_event_changes(item, z_idx)

    def _extract_battle_cmds(event_data, z_idx):
        """遞迴提取 afterBattle 命令，將 flag 條件轉為 if_flag + set_flag"""
        commands = []
        if isinstance(event_data, dict):
            etype = event_data.get('type')
            if etype == 'openDoor':
                locs = event_data.get('loc', [])
                if isinstance(locs, (list, tuple)) and len(locs) == 2 and not isinstance(locs[0], (list, tuple)):
                    locs = [locs]
                for loc in locs:
                    if isinstance(loc, (list, tuple)) and len(loc) == 2:
                        ex, ey = loc
                        commands.append({'type': 'open', 'loc': (z_idx, ey, ex)})
            elif etype == 'if':
                true_cmds = _extract_battle_cmds(event_data.get('true', []), z_idx)
                false_cmds = _extract_battle_cmds(event_data.get('false', []), z_idx)
                # 從 false 分支提取 flag 設置
                flag_set = None
                for cmd in false_cmds:
                    if cmd.get('type') == 'set_flag':
                        flag_set = cmd
                        break
                if true_cmds and flag_set:
                    cond = event_data.get('condition', 'True').strip()
                    if cond.startswith('flag:'):
                        cond = '>= 1'
                    commands.append({
                        'type': 'if_flag',
                        'name': flag_set['name'],
                        'condition': cond,
                        'true': true_cmds
                    })
                    commands.append(flag_set)
                else:
                    commands.extend(true_cmds)
                    commands.extend(false_cmds)
            elif etype == 'setValue':
                name = event_data.get('name', '')
                if name.startswith('flag:'):
                    try:
                        val = int(event_data.get('value', 1))
                    except ValueError:
                        val = 1
                    commands.append({
                        'type': 'set_flag',
                        'name': name,
                        'value': val
                    })
            else:
                for v in event_data.values():
                    commands.extend(_extract_battle_cmds(v, z_idx))
        elif isinstance(event_data, list):
            for item in event_data:
                commands.extend(_extract_battle_cmds(item, z_idx))
        return commands

    for idx, fid in enumerate(floor_ids):
        # events: 玩家踏上該格時觸發 → 直接使用觸發後狀態，應用到地圖
        events = raw['floors'][fid].get('events', {})
        for key, event_data in events.items():
            if not event_data:
                continue
            _apply_event_changes(event_data, idx)

        # afterBattle: 擊敗該格怪物後觸發 → 保留動態屬性
        after_battle = raw['floors'][fid].get('afterBattle', {})
        for key, event_data in after_battle.items():
            if not event_data:
                continue
            x_str, y_str = key.split(',')
            pos = (idx, int(y_str), int(x_str))
            cmds = _extract_battle_cmds(event_data, idx)
            if cmds:
                floors['afterEvent'].setdefault(pos, []).extend(cmds)

    # ------------------------------------------------------------------------
    #  後處理：刪除 MT2 小偷 NPC，指定位置改為 map_01 的 (0,1,10) 值
    # ------------------------------------------------------------------------
    for idx, fid in enumerate(floor_ids):
        src = raw['floors'][fid]['map']
        for y, row in enumerate(src):
            for x, c in enumerate(row):
                # MT2 刪掉小偷 NPC
                if idx == 1 and c == 123:
                    map_3d[idx][y][x] = 0

    # 指定位置改為 18（10層魔塔的 bluePotion）
    _custom_positions = [
        (0, 11, 10), (1, 10, 3), (1, 10, 4), (1, 11, 3),
        (2, 1, 5), (2, 2, 4), (2, 2, 6), (3, 2, 11),
        (6, 9, 9), (6, 11, 7), (7, 3, 9), (9, 11, 11)
    ]
    for z, y, x in _custom_positions:
        map_3d[z][y][x] = 18

    # MT10 骷髏兵布局修改
    map_3d[9][1][6] = 122   # (9,1,6) skeletonCaptain
    map_3d[9][4][5] = 120   # (9,4,5) skeleton
    map_3d[9][4][6] = 121   # (9,4,6) skeletonSoilder
    map_3d[9][4][7] = 120   # (9,4,7) skeleton
    map_3d[9][5][5] = 120   # (9,5,5) skeleton
    map_3d[9][5][6] = 121   # (9,5,6) skeletonSoilder
    map_3d[9][5][7] = 120   # (9,5,7) skeleton
    map_3d[9][6][5] = 120   # (9,6,5) skeleton
    map_3d[9][6][7] = 120   # (9,6,7) skeleton

    # MT10 刪除 END 節點，(9,3,6) 設為特殊門
    map_3d[9][11][6] = 0    # 刪除 END
    map_3d[9][3][6] = 40    # (9,3,6) specialDoor

    # MT10 動態門：擊敗 8 個骷髏兵後打開 (9,3,6)
    _mt10_skeleton_positions = [
        (9, 4, 5), (9, 4, 7), (9, 5, 5), (9, 5, 7),
        (9, 6, 5), (9, 6, 7), (9, 4, 6), (9, 5, 6)
    ]
    for pos in _mt10_skeleton_positions:
        floors['afterEvent'].setdefault(pos, []).extend([
            {'type': 'if_flag', 'name': 'mt10_skeletons', 'condition': '>= 7', 'true': [
                {'type': 'open', 'loc': (9, 3, 6)}
            ]},
            {'type': 'set_flag', 'name': 'mt10_skeletons', 'value': 1}
        ])

    npcs = {
        (3, 1, 6): 'shop',
        (3, 1, 6, 1): [
            {'type': 'if',
             'condition': 'player.money >= 25'},
            {'type': 'addValue', 'name': 'player.money', 'value': -25},
            {'type': 'addValue', 'name': 'player.hp', 'value': 800},
            {'type': 'not_activated'}],
        (3, 1, 6, 2): [
            {'type': 'if',
             'condition': 'player.money >= 25'},
            {'type': 'addValue', 'name': 'player.money', 'value': -25},
            {'type': 'addValue', 'name': 'player.atk', 'value': 4},
            {'type': 'not_activated'}],
        (3, 1, 6, 3): [
            {'type': 'if',
             'condition': 'player.money >= 25'},
            {'type': 'addValue', 'name': 'player.money', 'value': -25},
            {'type': 'addValue', 'name': 'player.def', 'value': 4},
            {'type': 'not_activated'}],
    }

    items = {
        'redPotion': {'cls': 'potion', 'hp': 50},
        'bluePotion': {'cls': 'potion', 'hp': 200},
    }
    return floors, {}, {}, player, items, npcs, {}


# ----------------------------------------------------------------------------
#  載入檔案其他數據
# ----------------------------------------------------------------------------
def load_data(name):
    attribute_list = []
    for x, y in zip(_default_data(), _difference_data(name)):
        # 優雅的合併更新字典，需要python3.5
        attribute_list.append({**x, **y})
    keys = ['floors', 'maps', 'enemies', 'player', 'items', 'npcs', 'icons']
    env_data = dict(zip(keys, attribute_list))
    return env_data


# ----------------------------------------------------------------------------
#  差異數據
#  以更新預設數據，來減少不同環境之間重複的數據。
# ----------------------------------------------------------------------------
def _difference_data(name):
    if name == '10層魔塔':
        return _load_mota_first10()
    raise RuntimeError('the database name does not exist.')



